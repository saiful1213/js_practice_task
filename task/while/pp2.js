// Subtask-1:
// Find all the odd numbers from 61 to 100.

let number = 61;

while (number <= 100) {
    if (number % 2 !== 0) {
        console.log(number)
    }
    number++
}



// Subtask-2:
// Find all the even numbers from 78 to 98.

let num = 78;

while (num <= 98) {
    if (num % 2 === 0) {
        console.log(num)
    }

    num++
}